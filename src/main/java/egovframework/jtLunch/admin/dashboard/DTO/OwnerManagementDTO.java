package egovframework.jtLunch.admin.dashboard.DTO;

public class OwnerManagementDTO {
	private String owner_id;
	private String owner_pw;
	private String owner_name;
	private String owner_tel;
	private String owner_auth;
	private String restaurant_id;
	private String name;
	
	public String getOwner_id() {
		return owner_id;
	}
	public void setOwner_id(String owner_id) {
		this.owner_id = owner_id;
	}
	public String getOwner_pw() {
		return owner_pw;
	}
	public void setOwner_pw(String owner_pw) {
		this.owner_pw = owner_pw;
	}
	public String getOwner_name() {
		return owner_name;
	}
	public void setOwner_name(String owner_name) {
		this.owner_name = owner_name;
	}
	public String getOwner_tel() {
		return owner_tel;
	}
	public void setOwner_tel(String owner_tel) {
		this.owner_tel = owner_tel;
	}
	public String getOwner_auth() {
		return owner_auth;
	}
	public void setOwner_auth(String owner_auth) {
		this.owner_auth = owner_auth;
	}
	public String getRestaurant_id() {
		return restaurant_id;
	}
	public void setRestaurant_id(String restaurant_id) {
		this.restaurant_id = restaurant_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}